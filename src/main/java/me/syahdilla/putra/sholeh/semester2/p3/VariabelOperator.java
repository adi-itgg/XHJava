package me.syahdilla.putra.sholeh.semester2.p3;

public class VariabelOperator {

    public static void main(String[] args) {
        int i; long j;
        i = 10; j = 0xc17L;
        System.out.println("Nilai Integer variabel i = " + i + "; variabel j = " + j);
        float x; double y;
        x = 10.50F; y = 17.15;
        System.out.println("Nilai Float variabel x = " + x + "; Double variabel y = " + y);
        char a; String b;
        a = 'D'; b = "Danang Sutrisno";
        System.out.println("inisial : " + a + "\nNama: " + b);
    }

}
